function afficherComparaison(persoPropose, persoADeviner) {
  const comparaison = document.querySelector(".tableau-contenu");

  let tableauHTML = `
    <table>
      <tbody>
        <tr class="contenu-ligne">
          <th class="ligne-img">
            <img class="comparaison-img" src="${persoPropose.image}">
          </th>
  `;

  // Ajout de la comparaison pour chaque attribut
  tableauHTML += Object.keys(persoADeviner)
    .filter(attribut => attribut !== "prenom" && attribut !== "nom" && attribut !== "image")
    .map(attribut => {
      const valPropose = persoPropose[attribut];
      const valADeviner = persoADeviner[attribut];
      let couleur;

      // Si les valeurs sont identiques
      if (JSON.stringify(valADeviner) === JSON.stringify(valPropose)) {
        couleur = "#2b9d57"; // Compl√®tement √©gal
      } 
      // Si valPropose et valADeviner ont une cha√Æne de caract√®res en commun
      else if (
        (Array.isArray(valADeviner) && typeof valPropose === "string" && valADeviner.some(v => v.includes(valPropose))) || // Si valPropose est une string et existe dans un √©l√©ment de valADeviner
        (Array.isArray(valPropose) && typeof valADeviner === "string" && valPropose.some(v => v.includes(valADeviner))) || // Si valADeviner est une string et existe dans un √©l√©ment de valPropose
        (Array.isArray(valADeviner) && Array.isArray(valPropose) && valPropose.some(v => valADeviner.includes(v)) && valADeviner.some(v => valPropose.includes(v))) // Si un √©l√©ment de valPropose est dans valADeviner et vice versa
      ) {
        couleur = "#dbb350";
      } else {
        couleur = "#87173a";
      }

      return `
          <th class="contenu-ligne-cell" style="background-color: ${couleur};">
          ${Array.isArray(valPropose) ? valPropose.join(", ") : valPropose || "Aucun"}
          </th>
      `;
    })
    .join('');

  tableauHTML += `</tr></tbody></table>`;

  // Ins√©rer le tableau dans le DOM
  comparaison.innerHTML = tableauHTML + comparaison.innerHTML;

  // Ajuster la taille du texte dans les cellules
  const cellules = document.querySelectorAll('.contenu-ligne-cell');

  cellules.forEach(cellule => {
    // R√©cup√©rer le texte de la cellule
    let texte = cellule.innerText || cellule.textContent;
  
    // Transformer en cha√Æne si c'est un tableau
    if (Array.isArray(persoPropose[texte])) {
      texte = persoPropose[texte].join(", ");
    }

    let tailleTexte = 14;
    // Ajuster la taille du texte pour respecter la dimension carr√©e des cellules
    if (((texte.split(' ').length > 2 && texte.includes(','))  // les string compos√©es de plus de 2 mots separ√©s par au moins une virgule (d√©bordement vertical)
      || (texte.split(' ').some(mot => mot.length > 10))) && !texte.includes('Voleur(se)')) { // les string contenant au moins un mot de + de 10 lettres (d√©bordement horizontal)
      tailleTexte = 10;
    } else if ((texte.split(' ').some(mot => mot.length > 9)) || texte.includes('Voleur(se)') || texte.includes('Gungi')) { // les string contenant au moins un mot de + de 9 lettres (d√©bordement horizontal) 
      tailleTexte = 13;
    } else if (texte.length < 5) { // Taille pour bien voir les string courtes
      tailleTexte = 18;
    }

    cellule.style.fontSize = `${tailleTexte}px`;
  })

  
}

